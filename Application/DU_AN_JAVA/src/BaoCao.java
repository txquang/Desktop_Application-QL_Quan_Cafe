    public void TinhTong ()
    {   float Tong = 0;
        int DEm = jTable1.getRowCount();
        for (int i = 0; i < DEm ; i++) 
        {
            String Tam = (String) jTable1.getValueAt(i, 5);
            Tong += Float.parseFloat(Tam);
        }
        DecimalFormat formatter = new DecimalFormat("#,###,###");
        String S = formatter.format(Double.valueOf(Tong));
        TongTienLB.setText(S);
    }
